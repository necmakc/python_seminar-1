# Задача 1: Найдите сумму цифр трехзначного числа.
# Пример:
# 123 -> 6 (1 + 2 + 3)
# 100 -> 1 (1 + 0 + 0) |

# -----main-----
number = int(input("Введите трехзначное число: "))
sum = 0
while (number != 0):
    sum += int(number % 10)
    number /= 10
print(f"Сумма чисел равна: {sum}")

