# ребенок, если известно, что Петя и Сережа сделали одинаковое количество журавликов, а Катя сделала в два раза больше
# журавликов, чем Петя и Сережа вместе?
# Пример:
# 6 -> 1  4  1
# 24 -> 4  16  4
# 60 -> 10  40  10
# petya, sergey, kate = None

# -----main-----
quantity = int(
    input("Введите количество журавликов: "))
if quantity == 5:
    petya = 1
    kate = 3
    sergey = 1
    print(petya, kate, sergey)
elif quantity < 5:
    print("Не подходит под условие или просто бездельники")
else:
    petya = int(quantity / 2 / 3)
    sergey = int(quantity / 2 / 3)
    kate = int(quantity - petya - sergey)
    print(petya, kate, sergey)


